function object2array($obj)
{
    $arr = is_object($obj) ? get_object_vars($obj) : $obj;
    foreach ((array) $arr as $key => $val) {
        $val = (is_array($val) || is_object($val)) ? object2array($val) : $val;
        $arr[$key] = $val;
    }
    return $arr;
}
